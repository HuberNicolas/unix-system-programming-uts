#!/usr/bin/python3
#Examples of raw string embedded formating
print(r"2 backslash      \\  characters")
print(r"a single quote   \'  character")
print(r"a double quote   \"  character")
print(r"a bell           \a  character") #This should not be audible.
print(r"a backspace      \b  character")
print(r"a form feed      \f  character")
print(r"a line feed      \n  character")
print(r"a carrage return \r  character")
print(r"a horizontal tab \t  character")
print(r"a vertical   tab \v  character")
print(r"a unicode 16bit  \u0394  character")
print(r"a unicode 32bit  \U00000394  character")

