#!/usr/bin/python3
#Examples of normal string embedded formating
print("a backslash      \\  character")
print("a single quote   \'  character")
print("a double quote   \"  character")
print("a bell           \a  character") #This should be audible.
print("a backspace      \b  character")
print("a form feed      \f  character")
print("a line feed      \n  character")
print("a carrage return \r  character")
print("a horizontal tab \t  character")
print("a vertical   tab \v  character")
print("a unicode 16bit  \u0394  character")
print("a unicode 32bit  \U00000394  character")

